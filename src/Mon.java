
public class Mon {
	private String maMon;
	private String tenMon;
	public Mon() {
		super();
	}
	public Mon(String maMon, String tenMon) {
		super();
		this.maMon = maMon;
		this.tenMon = tenMon;
	}
	public String getMaMon() {
		return maMon;
	}
	public void setMaMon(String maMon) {
		this.maMon = maMon;
	}
	public String getTenMon() {
		return tenMon;
	}
	public void setTenMon(String tenMon) {
		this.tenMon = tenMon;
	}
	
}
